            <br>·货到付款
            <br>·余额支付（手机支付、信用卡）
            <br>
            <br>注意：买家如果选择货到付款，商品配送到指定地点后，买家需要现场支付商品的所有金额，如果无法支付全部货款，那么我们的配送员将无法将货物交到您的手中。
