            <br>
            <br>退货时需要支付运费吗？
            <br>退回产品的运费根据退款原因定，由于平台产品质量问题发起退款，平台承担退货取件运费，若非商品质量原因办理退货，您将需要承担退货费用，费用将会从您的商品退款中直接扣除。
